use Illuminate\Support\Facades\Broadcast;

/*
|--------------------------------------------------------------------------
| قنوات البث (Broadcast Channels)
|--------------------------------------------------------------------------
|
| هنا يتم تسجيل قنوات البث التي يدعمها التطبيق (إن وُجدت).
| الدوال الموجودة هنا تتحقق من صلاحية المستخدم للاستماع إلى القناة.
|
*/

Broadcast::channel('App.Models.User.{id}', function ($user, $id) {
    return (int) $user->id === (int) $id;
});

// Summary: يحدد قواعد السماح بالاشتراك في قنوات البث للمستخدمين عبر مقارنة المعرفات.